function set_cookie(cookiename, cookievalue, hours) {
    var date = new Date();
    date.setTime(date.getTime() + Number(hours) * 3600 * 1000);
    document.cookie = cookiename + "=" + JSON.stringify(cookievalue) + "; path=/;SameSite=Strict;expires = " + date.toGMTString();
}

